void MPU6050_LettoreAccelerazione(void){
		HAL_I2C_Mem_Read(&hi2c2,MPU6050_ADDR, ACCEL_XOUT_H_REG, 1, recData1,6,1000);

		accelerazioneGrezzaX = (int16_t)(recData1[0]<<8 | recData1[1]);
		accelerazioneGrezzaY = (int16_t)(recData1[2]<<8 | recData1[3]);
		accelerazioneGrezzaZ = (int16_t)(recData1[4]<<8 | recData1[5]);

		AX = accelerazioneGrezzaX/16384.0;
		AY = accelerazioneGrezzaY/16384.0;
		AZ = accelerazioneGrezzaZ/16384.0;

	}

